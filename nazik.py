# Функция ввода и мини-игровые эффекты
def ask(question, trait, karma_effect):
    global karma
    while True:
        ans = input(question + "\nВыбор: ")
        if ans == "1":
            traits[trait] += 1
            karma += karma_effect
            break
        elif ans == "2":
            karma -= karma_effect
            break
        else:
            print("❌ Введи 1 или 2")

# Прохождение теста
